// Os operadores de comparação servem para comparar 2 ou mais valores e sempre retornam um booleano

// > Maior que
// < Menor que
// >= Maior ou igual a 
// <= Menor ou igual a 

// == Igual a
// === Estritamente igual a
// != Diferente de
// !== Estritamente diferente de

console.log(`5 é maior que 2? ${5 > 2}`); // true
console.log(`11 é menor que 13? ${11  < 13}`); // true
console.log(`22 é maior ou igual a 22? ${22 >= 22}`); // true
console.log(`1 é menor ou igual a 3? ${1 < 3}`); // true
console.log(`3 é igual a 3? ${3 == 3}`); // true
console.log(`15 é diferente de 12? ${15 != 12}`); // true
console.log(`"34" é estritamente igual a 34? ${"34" === 34}`); // false
console.log(`"98" é estritamente diferente de "98"? ${"98" !== "98"}`); // false